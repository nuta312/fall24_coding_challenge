package coding_bat.aiza.warm_up1;

public class in3050 {
    public boolean in3050(int a, int b) {
        boolean inRange3040 = (a >= 30 && a <= 40) && (b >= 30 && b <= 40);
        boolean inRange4050 = (a >= 40 && a <= 50) && (b >= 40 && b <= 50);
        return inRange3040 || inRange4050;
    }
}
